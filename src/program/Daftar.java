public class Daftar {
    private String usernameA;
    private String emailA;
    private String passA;
    private String passAgA;

    public String getUsernameA() {
        return usernameA;
    }

    public void setUsernameA(String usernameA) {
        this.usernameA = usernameA;
    }

    public String getEmailA() {
        return emailA;
    }

    public void setEmailA(String emailA) {
        this.emailA = emailA;
    }

    public String getPassA() {
        return passA;
    }

    public void setPassA(String passA) {
        this.passA = passA;
    }

    public String getPassAgA() {
        return passAgA;
    }

    public void setPassAgA(String passAgA) {
        this.passAgA = passAgA;
    }
}
